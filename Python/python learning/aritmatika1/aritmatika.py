

#Variable
a = 10
b = 3

# Pertambahan
hasil = a + b
print(a, '+' ,b , '=', hasil )


# Pengurangan -
hasil = a - b
print(a, '-' ,b , '=', hasil )


# Perkalian *
hasil = a * b
print(a, '*' ,b , '=', hasil )


# Pembagian /
hasil = a / b
print(a, '/' ,b , '=', hasil )


# eksponen (pangkat) **
hasil = a ** b
print(a, '**' ,b , '=', hasil )


# Modulus   %
hasil = a % b
print(a, '%' ,b , '=', hasil )

# Floor division //
hasil = a / b
print(a, '//' ,b , '=', hasil )





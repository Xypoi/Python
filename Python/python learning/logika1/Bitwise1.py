#Operator BitWise


A = 9
B = 5

#Bitwise Or(|)
C = A | B
print('\n===== OR ====')
print('nilai :',A,' , Binary :',format(A,'08b'))
print('nilai :',B,' , Binary :',format(B,'08b'))
print('--------------------------------( | )')
print('nilai :',C,' , Binary :',format(C,'08b'))


#Bitwise AND(&)
C= A & B
print('\n===== AND ====')
print('nilai :',A,' , Binary :',format(A,'08b'))
print('nilai :',B,' , Binary :',format(B,'08b'))
print('--------------------------------( & )')
print('nilai :',C,' , Binary :',format(C,'08b'))

#Bitwise XOR(^)
C = A ^ B
print('\n===== XOR ====')
print('nilai :',A,' , Binary :',format(A,'08b'))
print('nilai :',B,' , Binary :',format(B,'08b'))
print('--------------------------------( ^ )')
print('nilai :',C,' , Binary :',format(C,'08b'))


#Bitwise NOT(~)
C = ~B
print('\n===== NOT ====')
print('nilai :',A,' , Binary :',format(A,'08b'))
print('nilai :',B,' , Binary :',format(B,'08b'))
print('---------------------------------------')
print('nilai :',C,' , Binary :',format(C,'08b'))



